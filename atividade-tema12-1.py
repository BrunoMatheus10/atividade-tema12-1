# Par 1: 10 e 2
A = 10  # Inicializa a variável A com o valor 10
B = 2   # Inicializa a variável B com o valor 2
C = 0   # Inicializa a variável C com o valor 0
while A > B:  # Enquanto A for maior que B, execute o loop
    A = A - B  # Subtrai B de A e armazena o resultado em A
    C += 1     # Incrementa C em 1
print("Para (10, 2): C =", C, "A =", A)  # Imprime os valores finais de C e A

